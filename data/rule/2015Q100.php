		<h4>2.6	比赛规则</h4>
		<p>如下为简要规则说明，具体规则以《2015年“元鼎杯”京津冀MBA笼式足球联赛竞赛规则》为准。</p>
		<p>2.6.1	参赛队员名单（配照片）提交组委会备案，请注明领队及场上队长，可由队员兼任。（详见《2015年“元鼎杯”京津冀MBA笼式足球联赛队员报名表》）</p>
		<p>2.6.2	球队名单人数：每队报名参赛队员不得少于12人，不得超过30名。</p>
		<p>2.6.3	替补和换人：小组赛每场比赛最多可替换7名队员，淘汰赛每场比赛最多可替换10名队员。</p>
		<p>2.6.4	服装：比赛双方不得佩戴危及双方的危险物品,比赛双方只能穿胶底帆布鞋,皮质球鞋(硬钉球鞋), 禁止穿其他鞋上场.不符合上述规定者,将不许上场。每队尽可能统一比赛服装,如无统一服装,应统一颜色的深浅或同为深色或同为浅色,守门员服装应跟场上其他队员服装有明显区别，队员姓名与号码对应，不可变更号码。各队队长需佩戴队长袖标。</p>
		<p>2.6.5	特殊要求：场上队员严禁使用钢钉，铁钉及鞋钉大于8mm 的运动鞋；严禁任何形式的铲球动作，否则裁判有权判罚犯规，并依据情节出示黄牌或红牌。要求各参赛球员必须佩戴护腿板，否则不得上场比赛。上场球员不得佩戴戒指、项链、耳环等饰物。</p>
		<p>2.6.6	赛场规则：采用国家体育局颁布的《足球竞赛规则》和《全国足球比赛纪录规定》。参赛球队赛后有向比赛组委会申诉的权利，并由仲裁委员会仲裁。</p>
		<p>2.6.7	红黄牌规则：1 张红牌或累计2 张黄牌的队员停赛一场。小组赛的牌不累计进入淘汰赛。1 张红牌将处罚金500 元，2张黄牌以后，1 张黄牌将处罚金100 元。出现打架斗殴、罢赛等情况的球队，不论对错及事故起因，首先挑起事端的一方将立即被取消参赛资格并扣除所有保证金，通报所有MBA 院校批评，其他参与球队根据实际参与情况，由联赛仲裁委员会处理。参赛队伍对于处罚结果拥有向仲裁委员会申诉的权利。</p>
		
		<h4>2.7	裁判工作</h4>
		<p>2.7.1	联赛裁判均为外聘，设置一名主裁和一名第二裁判。</p>
		<p>2.7.2	参赛球队各派一名人员作为赛场监督，参与审核对方队员参赛资格、人员替换、犯规记录等。审核完毕后需要在审核表上签字，以示对此结果认可。</p>
		<p>2.7.3	每场球赛组委会均会派一名赛事督导监督比赛的整体情况，负责最终记录红黄牌、进球数、进球人员名单、比分等，并及时汇报到组委会。</p>
		<p>2.7.4	所有参赛队员必须无条件服从裁判的判罚。有不遵守比赛记录、对裁判不敬的行为的，裁判有权出示红、黄牌，且赛后有向组委会申诉、汇报的权利和义务。情节严重者，在所有MBA院校通报批评，并交由联赛仲裁委员会处理。</p>

