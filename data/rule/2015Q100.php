		<h4>1.2	比赛背景</h4>

		<p>MBA首创于1908年的美国哈佛，于1991年引进中国，是一种注重实战能力的教育。经过近一个多世纪的实践创造了举世瞩目的MBA神话。所谓的MBA精神主要指崇尚自由竞争和个人奋斗,崇尚冒险和创业,鼓励对事业的追求,强调勤奋工作以创造富有生活,强调个人能力、毅力、智慧和决心,注重理性分析,讲求实际和办事成效，在某种意义上，MBA精神也可以说是企业家和职业经理人精神的一个综合。</p>
		<p>作为中国高等教育体系中最特别的专业学位之一，MBA项目侧重于不断的培养管理技能、方法和信心，致力于敬业精神、团队精神的塑造，而想未来发展为职业经理人、企业家，则还应树立敢于承担责任、务实创新、善于调配各种资源，协调各种关系，豁达的胸怀、平和的心态等精神。所以，在每个人的具体学习、工作之外，各高校、各MBA社团在全中国范围内开展着各种各样的社会实践活动。</p>
		<p>作为中国MBA界的级别最高、规模最大、成员最多、影响最广的合作组织机构，北京MBA联盟一直注重社会实践方面活动的开发，以多种形式加强MBA与企业的深度联系；MBA学员、MBA联合会之间的相互联系。比赛的宗旨是：加强交流、拓展思维、弘扬文化、强健体魄。希望各参赛商学院将在这个交流平台上充分展现自身的魅力，并使各院校进一步加强了解、促进交流、增进友谊，同时我们也希望通过参赛院校的共同努力向社会各界集体展示我们MBA群体积极进取、朝气蓬勃的良好素质。</p>
		
		<h4>1.3	笼式足球介绍</h4>

		<p>笼式足球目前在中国日渐火热，越来越多的白领接受这种“快乐足球”。“笼式足球”又称为“蝎斗足球”，是一种全新的、时尚的动感的比赛方式，采用3 VS 3或5 VS 5比赛模式。比赛场地采用封闭式铁笼，没有边界，场地小、队员少，感官上更富有刺激性、更具观赏性。本届比赛采用5 VS 5比赛模式。</p>
		
