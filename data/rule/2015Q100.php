		<p>本届联赛组委会由北京MBA联盟及北京邮电大学MBA联合会负责组成，各参赛代表院校选派一名负责人组成赛事监督委员会及仲裁委员会。（详见《2015年“元鼎杯”京津冀MBA笼式足球联赛筹备组主要联系人》）</p>
		<p>各参赛队伍需尊重并服从组委会工作组织，对组委会工作有异议需报赛事监督委员会讨论，不得对组委会工作进行干扰。</p>
		
		<h4>3	比赛赛程</h4>
		<h4>3.1	赛程说明</h4>
		<p>2015年“元鼎杯”京津冀MBA笼式足球联赛组委会综合考虑了比赛环境,赛程安排等客观因素,本着公平竞争的竞赛原则,先将本年度赛程初拟如下：</p>
		<p>第一届京津冀MBA笼式足球联赛共分小组赛、复活赛、淘汰赛三个阶段,按照24支球队计算，暂定初赛自2015年10月11日开始,其中比赛日(每周六周日)共 12个,小组赛6组3回合，预计36场比赛，复活赛4场，1/8决赛8场、1/4决赛4场、半决赛2场、三四名决赛1场、决赛1场，比赛共进行 56 场比赛。</p>
		<p>3.1.1	小组赛/复活赛</p>
		<p>分组：S支球队由抽签决定分组, 小组赛设N支种子队（东道主、上届京津冀联赛前四名、由近及远的往届冠军球队）分在N个小组中， 共N组，每组Y支球队。小组抽签暂定于9月X日14:00，在北京市海淀区西土城路10号北京邮电大学经济管理学院举行，请各学校至少派一名代表前来抽签，如不能前来，请委托他校同学代替进行抽签。</p>
		<p>晋级规则：每个小组的前两名（共计2N支球队）以及复活赛获胜的M支球队，共16队进入淘汰赛。</p>
		<p>3.1.2	1/8决赛</p>
		<p>共16队进入晋级淘汰赛阶段，进入淘汰赛阶段后，按照成绩进行总排名（排名规则参照小组赛），分为8组进行比赛。</p>
		<p>具体对阵情况如下：</p>
		<p>第1名 vs 第16名  1/8决赛1组<br />第8名 vs 第9名   1/8决赛2组</p>
		<p>第4名 vs 第13名  1/8决赛3组<br />第5名 vs 第12名  1/8决赛4组</p>
		<p>第2名 vs 第15名  1/8决赛5组<br />第7名 vs 第10名  1/8决赛6组</p>
		<p>第3名 vs 第14名  1/8决赛7组<br />第6名 vs 第11名  1/8决赛8组</p>
		<p>3.1.3	1/4决赛</p>
		<p>1组胜者 vs 2组胜者 1/4决赛1组</p>
		<p>3组胜者 vs 4组胜者 1/4决赛2组</p>
 
		<p>5组胜者 vs 6组胜者 1/4决赛3组</p>
		<p>7组胜者 VS 8组胜者 1/4决赛4组</p>
