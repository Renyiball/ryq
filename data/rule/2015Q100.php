		<h4>2.2	赛程安排</h4>

		<p>2.2.1	赛程按惯例排成小组赛、复活赛和淘汰赛，各个院校经抽签对应到各小组。</p>
		<p>2.2.2	第一阶段——小组赛，根据报名学校数量分为若干组，每4个学校为一组。天津河北地区一组。每组成绩最好的两支队伍进入淘汰赛阶段（十六强）。</p>
		<p>2.2.3	第二阶段——复活赛，成绩最好的前M个小组第三名进入复活赛A组，未直接晋级淘汰赛和复活赛A组的队伍，取网上支持率前M名进入复活赛B组。复活赛A组的球队通过抽签的方式逐一抽取复活赛B组的球队，以单场淘汰赛的方式决出最后M个晋级淘汰赛的队伍。</p>
		<p>M=16-2*实际参赛的小组，如果实际有6个小组，则M=16-2*6=4</p>
		<p>成绩最好的M个第三名的确定方法如下：</p>
		<p>所有小组的第三名按照积分排序，取前M名晋级。</p>
		<p>若积分相同，净胜球多者晋级。</p>
		<p>若积分和净胜球数相同，进球数多者晋级。</p>
		<p>若积分、净胜球数和进球数均相同，抽签决定晋级复活赛队伍。</p>
		<p>2.2.4	第三阶段——淘汰赛，包括1/8决赛、1/4决赛、半决赛、决赛。</p>
 
