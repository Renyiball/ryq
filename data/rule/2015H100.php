		<h1>2015年顺义区第五届“阳光保险杯”<br/>超级联赛决定名次办法</h1>
	
	 
		
		<div>
			<h3>一、比赛规则：</h3>
			<p>1.比赛分为小组赛（A、B组）和淘汰赛两个阶段。</p>
			<p>2.每场比赛上下每个半场45分钟，中场休息不得超过15分钟，常规时间90分钟内分出胜负，胜者积3分，负者积0分。若常规时间内未分出胜负，采用互罚点球决出胜负，胜者积2分，负者积1分。</p>
		</div>
		
		<div>
			<h3>二、比赛名次：</h3>
			<p>（一）小组赛采用单循环：</p>
			<p>1.按积分排名，积分相同的队伍比较相互之间的胜负关系。</p>
			<p>2.每个小组的1-4名晋级淘汰赛，5-8名进行排位赛。</p>
			<p>（二）淘汰赛采用直接淘汰制：A、B组交叉进行，小组前四的队伍进行单淘汰制，胜者晋级，负者则进行排位赛。</p>
			<p>（三）每场比赛每队换人不得超过7个，换下的球员不可以再上场。</p>
			<p>（四）比赛中运动员被裁判出示红牌或者累计三张黄牌自然停止下一场比赛。</p>
			<p>（五）如果一个队在比赛中不足7人，比赛自然终止，该队为弃权，判对方3:0胜。</p>
			<p>（六）每场比赛队长须佩戴队长袖标。</p>
			<p>（七）如有遇到特殊情况，或者天气的原因，原定比赛时间和场地可能会有所变动，一切听从组委会安排。</p>
		</div>
		
		<div>
			<h3>三、奖项：</h3>
			<p>比赛设有冠、亚、季军、最佳射手、公平竞赛等奖项。</p> 
