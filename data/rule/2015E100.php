		<h1>比赛办法</h1>
		
		<div>
			<h3>五、比赛办法</h3>
			<p>1. 比赛用球为5号球。一旦确定比赛用球，非经主办单位同意不得更改用球。比赛进行中，未经裁判员允许不得更换球。裁判员有权检查比赛用球，确定比赛用球。在比赛中发生球破裂或损坏，裁判员根据规则换球和重新开始比赛。各参赛队比赛中踢失的比赛用球将在下一场比赛前赔偿等值比赛用球，否则扣除相等数额联赛保证金。</p>
			<p>2. 队员人数</p>
			<p>（1）替换队员次数不受限，可以重复替换上场。</p>
			<p>（2）守门员可以和任何队员互换位置，但必须报告裁判员，裁判示意换人才能换人，并在死球时互换。</p>
			<p>（3）换人时，发球方需听裁判哨声发球。</p>
		</div>
		
		<div>
			<h3>六、队员装备</h3>
			<p>1. 每队必须比赛服装统一、护具齐全，穿布面胶底鞋、软皮面训练鞋、碎钉鞋、体操鞋 (包括守门员)，上场队员必须戴护腿板，违者不得上场比赛。参赛队员一律不得配戴首饰物品。</p>
			<p>2. 运动上衣（颜色相同，有号码），短裤（同一颜色）。守门员上衣颜色与队员区别，可穿长运动裤。</p>
			<p>3. 违反上述规定裁判员可令其更换，不更换不准上场。</p>
		</div>
		
		<div>
			<h3>七、犯规与不正当行为</h3>
			<p>1. 本次比赛禁止铲球，规则上如果队员草率地、鲁莽地或使用过分的力量在双方进行争抢或对方队员控制球时实施铲抢，被视为严重犯规，判给对方任意球，给予黄牌警告，情节严重将红牌罚出场。</p>
			<p>2. 拼抢过程中身体接触不允许抬肘后扬。</p>
			<p>3. 没有越位犯规的判罚。</p>
			<p>4. 同一场比赛同一队员被出示两张黄牌应同时出示红牌罚该队员出场。被红牌罚出场的队员不能坐在替补席上。同时不能重新进场比赛，他所属的球队应在队员被罚出场2分钟后或者当人多的球队进球的时候可以再重新补充队员上场（上场队员站到替换区由裁判示意允许方可进场）。</p>
		</div>
		
		<div>
			<h3>八、任意球、点球、角球、球门球、界外球</h3>
			<p>1. 任意球</p>
			<p>任意球全部为间接任意球，间接任意球直接入门不算得分，除非球入门前碰任何球员进门可算得分。</p>
			<p>罚球程序：</p>
			<p>a. 拼抢过程中身体接触不允许抬肘后扬。</p>
			<p>b. 没有越位犯规的判罚。</p>
			<p>c. 同一场比赛同一队员被出示两张黄牌应同时出示红牌罚该队员出场。被红牌罚出场的队员不能坐在替补席上。同时不能重新进场比赛，他所属的球队应在队员被罚出场2分钟后或者当人多的球队进球的时候可以再重新补充队员上场（上场队员站到替换区由裁判示意允许方可进场）。</p>
			<p>罚则：</p>
			<p>a. 球在踢出前对方进入距球8米以内，裁判员应该罚球延至符合规则规定后再开出，对进入8米内的对方球员给予警告。</p>
			<p>b. 球踢出后没有碰到本方队员或对方队员、踢任意球者再次触球示为连踢，判给对方在原地点踢间接任意球。</p>
			<p>c. 在对方罚球区内踢任意球，球应放在距犯规地点最近的罚球区线上进行。</p>
			<p>2. 点球</p>
			<p>罚球程序：</p>
			<p>a. 球放在罚球点上。</p>
			<p>b. 除主罚队员外，其余双方队员站在罚球区外，并到至少距罚球点5米。</p>
			<p>c. 对方未踢球前，守门员必须站在两门柱之间的球门线上。</p>
			<p>d. 罚球队员一脚站定，不能跳动、移动、滑动，点球直接入门算得分,点球未进无补射，判对方球门球。</p>
			<p>罚则：</p>
			<p>守方队员犯规，球未罚中应重罚。</p>
			<p>踢点球以外的攻方队员犯规，罚中无效，应重罚。</p>
			<p>3. 界外球</p>
			<p>当球的垂直投影从地面或空中越过边线后，由对方在球出界处踢界外球恢复比赛。</p>
			<p>踢球程序：</p>
			<p>a. 界外球手抛。</p>
			<p>b. 必须在6秒钟内将球发出，踢界外球未触碰任何球员直接进门不算得分。</p>
			<p>罚则：</p>
			<p>a. 发球队员违反发界外球规定，由对方在原处踢界外球。</p>
			<p>b. 发球队员发出的球没有触到对方队员再次触球视为连踢，判给对方在发生位置踢间接任意球。</p>
			<p>4. 球门球</p>
			<p>球的整体从地面或空中越过球门柱外的球门线，而最后踢或触球是攻方队员，判由守方发球门球。</p> 
			<p>罚球程序：</p>
			<p>a. 守方守门员把球放在本方罚球区线上。</p>
			<p>b. 发球门球时，对方队员在球未被发出罚球区之前应站在罚球区外距离球8米外。</p>
			<p>罚则：</p>
			<p>a. 球未出罚球区，任何队员踢或触及无效，重新发球门球。</p>
			<p>b. 球被发出罚球区后未被其他队员踢或触及，该发球队员再次触及球时，判由对方在守门员触球处最近的球门区线上踢间接任意球。</p>
			<p>c. 守门员在罚球区内可以用手接对方来球和本方头顶回传球。但不能用手接本方队员用脚有意回传的球。违者判由对方在距球位置最近的球门区线上踢间接任意球。</p>
			<p>d. 守门员无论何时用手控球时，持续时间都不得超过10秒。</p>
			<p>e. 守门员可用脚接同队队员直接发出的界外球。</p>
			<p>5. 角球</p>
			<p>球的整体从地面或空中越过球门柱外的球门线后，而最后踢或触球的是守方队员，判为攻方踢角球。</p>
			<p>踢角球的程序：</p>
			<p>a. 将球放定在球出线所在的球门线与边线的交点上。</p>
			<p>b. 对方队员要距球8米。</p>
			<p>罚则：</p>
			<p>a. 角球踢出后未经其他队员踢或触及，踢球队员再次触球，判由对方在犯规地点踢间接任意球。</p>
			<p>b. 如果角球已合法放好位置，但踢球队员未能在6秒钟内将球踢出，判由对方在原处踢间接任意球。</p>
