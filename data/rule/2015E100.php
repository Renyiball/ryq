		<h1>竞赛规程</h1>
		
		<div>
			<h3>一：比赛名称</h3>
			<p>2015奥森南园“任意球”&“铭人杯”七人制足球夏季冠军联赛</p>
		</div>
		
		<div>
			<h3>二：比赛时间</h3>
			<p>2015年7月15日—2015年09月30日</p>
		</div>
		
		<div>
			<h3>三：比赛方法</h3>
			<p>（一）采用单循环赛制，积分最高者获得冠军；</p>
			<p>1．每队胜一场得 3分，平一场得 1分，负一场得 0 分。</p>
			<p>2．如遇两队或两队以上积分相等时，则依次按相等队之间比赛的胜负、净胜球、总进球数决定名次，多者名次列前。</p>
			<p>3. 如上述各项仍相等，则依次按他们在全部比表中的胜负、净胜球、总进球数决定名次，多者名次列前。</p>
			<p>4. 如再相等，则以抽签的办法次定名次。</p>
			<p>（二）每队必须备有两套颜色不同的比费服装和护抹，<span>穿布面胶底鞋或软皮面训练鞋或体操鞋，TF (包括守门员)，严禁穿AG FG HG等带钉球鞋，</span>上场队员必须戴护腿板，比赛服装和护袜颇色全队必须一致，违者不得上场比赛。参赛队员一律不得配戴首饰物品。</p>
			<p>(三)不可抗拒的原因，由赛事组委会做出更改比赛日期的决定。</p>
		</div>
		
		<div>
			<h3>四、比赛时间、地点</h3>
			<p>暂定于7月18日，奥林匹克森林公园南园体育园七人制灯光球场。</p>
		</div>
		
		<div>
			<h3>五、比赛规程</h3>
			<p>（一）执行国际足联最新审定的《七人制足球赛竞赛规则》。</p>
			<p>（二）执行《全国体育竞赛纪律处罚规定》及《中国足协足球比赛违规违纪处罚方法》。</p>
			<p>（三）比费场地场地长46 - 48米，宽26 -28米，球门高度为 2 米，宽度为 3米，在室外赛场比赛。</p>
			<p>（四）比赛监督和裁判员由组委会选派。</p>
			<p>（五）10支球队以下全场比赛时间为 80分钟 ，上下半场 40分钟无商停补时。上下半场之间休息不得超过 5 分钟，比费用球为国际标准 5号球。</p>
			<p>（六）上场队员7 人，其中 1 人为守门员. 场上队员少于 3人，则比赛无效. 场上队长须自备与上衣颜色有明显区别的袖标。</p>
			<p>（七）<span>由于比赛场地小，所以无越位。</span></p>
		</div>
		
		<div>
			<h3>六、资格审查</h3>
			<p>（一）为端正赛风，突出体育比赛“团结、奋进、文明、育人”的宗旨，本次比赛成立联赛资格、纪律监察委员会，对所有报名参赛运动员的资格进行严格审查。</p>
			<p>（二） 对在比赛前发现不符合参赛资格者，将取消其比赛权利，并不得改报他人；对在比赛中、比赛后发现有不符合比费资格者，将取消本人和其所在队的比赛资格和成绩。</p>
		</div>
		
		<div>
			<h3>七、其他事宜</h3>
			<p>（一）为了加强对联赛的管理工作，确保本联赛各项工作的顺利进行，比赛的各参赛队在报到时须向赛会交纳“<span>抵押保证金人民币1000元</span>”，对于在比赛期间未违反赛会有关纪律规定、社会治安管理条例以及运动员参赛资格等问题的参赛队，其所交纳的“抵押保证金”将如数退还。</p>
