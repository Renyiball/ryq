		<h1>比赛细则</h1>
		
		<div>
			<h3>一、比赛前</h3>
			<p>1. 两队在开赛前15分钟到场，队员以队为单位交参赛证；无故开场10分钟还未到场者，视为弃权；若各球队的比赛时间有紧急事件不能正常举行比赛时，需提前三天向组委会反映，由组委会统一安排协调；如不经组委会同意，擅自作主不参加比赛的，将视为弃权，得0分；</p>
			<p>2. 比赛开始前，队长应在记录表上签字，代表球队参与挑边；</p>
			<p>3. 赛前裁判执行掷硬币决定场地权；</p>
			<p>4. 每次比赛时，各队球员务必带上参赛证，经双方核对无误后，方可开赛；一经发现球员冒名顶替，当即取消该队参加比赛的资格；比赛结果判另一方参赛队赢；</p>
		</div>
		
		<div>
			<h3>二、比赛中</h3>
			<p>1. 比赛时间：比赛由上下两个半场组成，10支球队方案每个半场各40分钟，换人不补时；半场之间间隔5分钟供队员休息。</p>
			<p>2. 球队人数：每队报名队员不超过18人，其中包括一名队长；只有登录在记录表上的球员，方可参加比赛，一经队长在记录表上签名后，即不得更换；场上队员为7人，1人为守门员，替补队员不得超过11人，比赛无越位限制。</p>
			<p>3. 比赛胜负：比赛结束时，进球多的队为胜队；</p>
			<p>4. 比赛中只有领队、队长有权向工作人员询问比赛相关事宜。</p>
			<p>5. 比赛中严禁铲球，一旦出现铲球动作，将根据实际情况给予黄牌或红牌惩罚，如果滑倒由当值裁判视情况而定。</p>
		</div>
		
		<div>
			<h3>三、比赛后</h3>
			<p>若对裁判的判罚有异议，队长可以在比赛后将异议记载于记录表上以作为抗议的记录，交仲裁委员会仲裁如在比赛进行时干扰裁判判罚，裁判应出示红黄牌或终止比赛；</p>
		</div>
		
		<div>
			<h3>四、运动员精神行为</h3>
			<p>1. 参加者必须了解比赛规则并遵守之；</p>
			<p>2. 参加者必须表现出绅士的风度，服从裁判员的判决，不与裁判或犯规运动员争执；</p>
			<p>3. 有关疑问可经由比赛队长要求解释，如有与裁判发生争执者将被处罚；</p>
			<p>4. 如比赛中出现争执，应找有关负责人进行调解；</p>
			<p>5. 如出现打架等违纪行为而影响比赛的，一律取消比赛资格并按规定给予处罚，情节严重者送交公安机关处理；</p>
			<p>6. 惩罚部分</p>
			<p>（1）队员如果对裁判的裁决不满意者，由队长向裁判交涉，任何人不得在场有任何有碍比赛正常进行的行为，如辱骂裁判，辱骂对方队员的一切出现严重违规违纪现象；一旦有以上情况发生，取消所在球队本场比赛资格，以0分计；扣除一定数额保证金。</p>
			<p>（2）队员在比赛时，有意伤害或者侮辱对方队员、裁判、组织人员者，裁判有权勒令其退出比赛，并且比赛计时停止；当本队队员被红牌罚下时球队在2分钟内不得换人顶替，受罚时间到后经裁判允许后方可入场。在受罚期间如果对方进球双方场上人数将自动恢复为5人。被红牌罚下的队员取消本场比赛资格，但可以参加下场比赛</p>
			<p>（3）一旦比赛中出现未按照前文规定罚下上场队员比赛，取消所在球队本场比赛资格，以0分计；</p>
			<p>（4）各队必须按照比赛规定准时入场，迟到10分钟者，视弃权论处；</p>
			<p>（5）拉拉队在为自己球队呐喊助威、加油时不得有谩骂、攻击对方球员的行为，应文明观看，如果拉拉队有意妨碍比赛正常进行的行为，裁判可出示红黄牌警告，严重者取消该队比赛资格；</p>
			<p>（6）一旦在比赛过程中如有一方因对裁判判罚不满，而消极抗赛或罢赛，取消所该队本场比赛资格，以0分计。</p>
			<p>（7）比赛如果出现以下情况裁判员将直接出示红牌将该队员罚出场：队员使用过大危险犯规动作以及报复性犯规动作；恶意辱骂及推搡对方队员和裁判 ；之前曾被警告后没有改正；违反体育道德等情况。（具体判罚将由裁判根据场上实际情况自行掌握，其他队员不得干涉裁判的判罚，要严格遵守）。各队队员要尊重对手，尊重裁判，如果球队出现辱骂、打架等暴力事件，将立即取消该球队的比赛资格，并扣除球队剩余比赛费用和所有保证金，情节严重的移交公安机关。</p>
		</div>
		
