font-family:宋体">第四章 竞赛规则及相关规定<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="margin-left:42.0pt;text-indent:-42.0pt;mso-char-indent-count:
-3.5;line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;
line-height:150%;font-family:宋体">一、比赛规则：执行国际足联最新审定的《足球竞赛规则》；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="margin-left:42.0pt;text-indent:-42.0pt;mso-char-indent-count:
-3.5;line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;
line-height:150%;font-family:宋体">二、比赛用球：使用国际标准<span lang="EN-US">5</span>号球；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">三、资格审查：各参赛队需在比赛开始前至少提前半小时到场并提交首发名单（每队每场比赛至多允许<span lang="EN-US">4</span>名外籍球员同时出场），并在比赛开始前由领队收齐上场队员的参赛证及有效身份证件，交至裁判席处进行资格审查，未通过资格审查者不得上场。参赛队进行换人时需由领队持换上队员的参赛证及有效身份证件，交至裁判席处进行资格审查，未通过资格审查者不得上场；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">四、队员人数：到规定的比赛开始时间时，若某队到场队员不足<span lang="EN-US">7</span>人不得开始比赛，由裁判员开表计时，十五分钟后仍不满足人数要求则判该队该场比赛<span lang="EN-US">0</span>比<span lang="EN-US">3</span>负。若比赛进行中，某队场上队员人数不满足要求，裁判员立即停止比赛，判该队该场比赛<span lang="EN-US">0</span>比<span lang="EN-US">3</span>负（若比赛停止时对手净胜球数超过<span lang="EN-US">3</span>球则保留当时比分）；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">五、队员替换：每队每场比赛允许替换<span lang="EN-US">7</span>名队员，每队大名单内除当场比赛首发队员外的其余队员均视为替补队员（停赛、禁赛球员除外），换下不得再换上，停赛、禁赛球员不得出场；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">六、装备要求：参赛队必须统一穿着组委会配发的比赛服（含短裤、球袜），所有上场队员须穿戴护腿板，不允许佩戴普通眼镜，只允许佩戴隐形眼镜或运动眼镜，不允许穿钢钉足球鞋；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">七、特殊情况：比赛开始后如遇不可抗拒因素致比赛中断，当时比赛成绩有效，组委会与双方协调解决；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">八、红黄牌停赛：同一队员累计得到<span lang="EN-US">3</span>张黄牌自然停赛一场，得到<span lang="EN-US">1</span>张红牌自然停赛一场。若同一队员在同一场比赛中得到两张黄牌被红牌罚下，下场比赛自动停赛一场，导致红牌的两张黄牌警告即告撤销。若同一队员在同一场比赛中得到一张黄牌后又得到一张直接红牌，下场比赛自动停赛且黄牌继续累计，若此前该队员已经累计得到两张黄牌，则该队员需要自然停赛两场。如果一名队员在比赛中被裁判员直接出示红牌，即当场停止比赛并自然停止下一场比赛。如纪律委员会对得到红牌的队员没有追加处罚，则该队员在停赛结束后即可恢复比赛。小组赛阶段红黄牌在小组赛结束后不清零；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体;color:black;mso-themecolor:
text1">九、赛场管理：组委会将按照各参赛队实际报名情况配发教练组工作证及球员参赛证，赛事期间须持证入场，证件仅限本人使用，严禁借用、冒用，违者没收证件并处罚款人民币<span lang="EN-US">1000</span>元。<span lang="EN-US"><o:p></o:p></span></span></p>

