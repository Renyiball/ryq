<p class="MsoNormal" align="center" style="margin-left:24.0pt;text-align:center;
text-indent:-24.0pt;mso-char-indent-count:-2.0;line-height:150%;mso-layout-grid-align:
none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">第五章 弃权、罢赛与退赛的认定及处罚办法<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">一、若某队有不符合参赛规定的运动员出场比赛，该队该场比赛按弃权处理；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">二、若某队队员人数不符合第四章第四条要求，该队该场比赛按弃权处理；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">三、出现下列情况者视为罢赛：<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span lang="EN-US" style="font-size:12.0pt;line-height:150%;font-family:宋体">1. </span><span style="font-size:12.0pt;line-height:150%;font-family:宋体">并非因不可抗拒因素且未获得组委会批准，未参加赛程规定的比赛；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span lang="EN-US" style="font-size:12.0pt;line-height:150%;font-family:宋体">2. </span><span style="font-size:12.0pt;line-height:150%;font-family:宋体">拒绝按照裁判员要求，在<span lang="EN-US">5</span>分钟内恢复中断的比赛。<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">四、若某队弃权或罢赛，则判该队该场比赛<span lang="EN-US">0</span>比<span lang="EN-US">3</span>负，（若比赛中止时对方净胜球数超过三球，则保留当时比分），双方球队弃权或罢赛，双方球队该场比赛均计<span lang="EN-US">0</span>分；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">五、若某队中途退赛，则该队所有比赛均判<span lang="EN-US">0</span>比<span lang="EN-US">3</span>负（在该队已进行的比赛中，若对方净胜球数超过三球，则保留当时比分）；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">六、如有弃权、罢赛及退赛情况发生，裁判组需根据实际情况向纪律委员会提交报告，由纪律委员会做出纪律处罚；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">七、组委会将对弃权、罢赛及退赛球队进行通报批评，取消其精神文明奖评选资格，没收全部押金。<span lang="EN-US"><o:p></o:p></span></span></p>

