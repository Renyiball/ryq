<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">二、个人奖项：赛事设立最佳射手、最佳守门员、最佳教练员及最佳裁判员等个人奖项；<span lang="EN-US"><o:p></o:p></span></span></p>

