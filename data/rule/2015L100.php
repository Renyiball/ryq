<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">三、小组赛阶段，根据往届京超排名情况从<span lang="EN-US">24</span>支参赛队中选出<span lang="EN-US">4</span>支作为第一档种子队，选出另外<span lang="EN-US">4</span>支作为第二档种子队，两档种子队顺序抽签落位，其他<span lang="EN-US">16</span>支球队混抽；<span lang="EN-US"><o:p></o:p></span></span></p>

