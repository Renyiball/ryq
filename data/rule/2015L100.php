<p class="MsoNormal" style="margin-left:42.0pt;text-indent:-42.0pt;mso-char-indent-count:
-3.5;line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;
line-height:150%;font-family:宋体">一、组委会将在比赛场地内配备救护车及急救人员；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="margin-left:30.0pt;text-indent:-30.0pt;mso-char-indent-count:
-2.5;line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;
line-height:150%;font-family:宋体">二、各参赛队领队需对球员进行安全文明教育，保证比赛顺利进行；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">三、比赛进行期间，除赛场工作人员、裁判组成员、正进行比赛的参赛队教练组与运动员及新闻媒体人员外，其他人员不得进入比赛场地区域；<span lang="EN-US"><o:p></o:p></span></span></p>

<p class="MsoNormal" style="line-height:150%"><span style="font-size:12.0pt;
line-height:150%;font-family:宋体">四、若参赛队在比赛期间出现打架、斗殴行为，组委会将对涉事球队进行通报批评并扣除涉事参赛队全部押金，情节严重者移送公安机关处理；</span></p>

<p class="MsoNormal" style="line-height:150%;mso-layout-grid-align:none"><span style="font-size:12.0pt;line-height:150%;font-family:宋体">五、参赛队对其所属人员家属、啦啦队负有管理义务，如上述人员在赛场周边<a name="_GoBack"></a>出现谩骂、打架斗殴等行为，组委会将对该队处以重罚；<span lang="EN-US"><o:p></o:p></span></span></p>

