<p class=MsoNormal style='text-indent:21.0pt;layout-grid-mode:char;punctuation-wrap:
simple;text-autospace:none'><span style='font-family:宋体'>三、<span lang=EN-US>2015</span>校友杯足球赛结束后，根据实际支付情况，保证金的余额或全部返还给参赛队。</span></p>

<p class=MsoNormal style='text-indent:21.0pt;layout-grid-mode:char;punctuation-wrap:
