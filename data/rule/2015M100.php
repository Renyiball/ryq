<p class=MsoNormal align=center style='margin-top:12.0pt;margin-right:0cm;
margin-bottom:12.0pt;margin-left:0cm;text-align:center;layout-grid-mode:char;
punctuation-wrap:simple;text-autospace:none'><b><span style='font-size:14.0pt;
font-family:黑体'>第二章<span lang=EN-US>&nbsp; </span>参赛资格、报名</span></b></p>

<p class=MsoNormal style='margin-top:6.0pt;margin-right:0cm;margin-bottom:6.0pt;
margin-left:0cm;layout-grid-mode:char;punctuation-wrap:simple;text-autospace:
none'><span lang=EN-US style='font-family:宋体'>&nbsp;&nbsp;&nbsp; </span><b><span
style='font-family:宋体'>第五条</span></b><span lang=EN-US style='font-family:宋体'>&nbsp;
</span><span style='font-family:宋体'>资格：参赛球队主体成员构成为同一所高校的在京毕业生，包含大专、本科、研究生、博士生和在校教师。不允许本校本科、大专在校生参赛，不允许（前）专业或（前职业）运动员参赛。一场比赛中每队仅允许一名外援参加，外援必须为其他院校的大学毕业生。</span></p>

<p class=MsoNormal style='text-indent:21.0pt;layout-grid-mode:char;punctuation-wrap:
simple;text-autospace:none'><span lang=EN-US style='font-family:宋体'>&nbsp;</span></p>

<p class=MsoNormal style='text-indent:21.0pt;layout-grid-mode:char;punctuation-wrap:
simple;text-autospace:none'><b><span style='font-family:宋体'>第六条</span></b><span
lang=EN-US style='font-family:宋体'>&nbsp; </span><span style='font-family:宋体'>符合下述条件的运动员可以报名参加比赛：</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>一、</span><span style='font-family:宋体'>正规大学毕业生，包含大专、本科、研究生、博士生和在校教师；
</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>二、</span><span style='font-family:宋体'>年满<span
lang=EN-US>18</span>周岁，身体健康，无不宜于参加足球比赛疾病及隐患者； </span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>三、</span><span style='font-family:宋体'>在北京校友体育大联盟完成球员注册；</span></p>

<p class=MsoNormal style='text-indent:21.0pt;layout-grid-mode:char;punctuation-wrap:
simple;text-autospace:none'><b><span lang=EN-US style='font-family:宋体'>&nbsp;</span></b></p>

<p class=MsoNormal style='text-indent:21.0pt;layout-grid-mode:char;punctuation-wrap:
simple;text-autospace:none'><b><span style='font-family:宋体'>第七条</span></b><span
lang=EN-US style='font-family:宋体'>&nbsp; </span><span style='font-family:宋体'>报名</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>一、</span><span style='font-family:宋体'>球队在每年比赛开始前<span
lang=EN-US>30</span>天至<span lang=EN-US>10</span>天的期间内，向赛事委员会报名。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>二、</span><span style='font-family:宋体'>球队名称应报全称及简称：</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（一）</span><span style='font-family:宋体'>全称为：北京＋高校名＋球队名＋足球队；如：北京武汉大学珞珈山足球队；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（二）</span><span style='font-family:宋体'>简称为：北京＋高校名＋校友队（简称不应超过<span
lang=EN-US>9</span>个字）；如：北京武大校友队；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（三）</span><span style='font-family:宋体'>球队名称以在北京校友体育大联盟注册的名称为准，不得在报名时自行变更；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（四）</span><span style='font-family:宋体'>参赛球队按要求填写参赛人员报名表，提交参赛人员有效身份证电子版，大学毕业证、学位证或教师工作证电子版，近期清晰免冠照片电子版；</span></p>

<p class=MsoNormal style='layout-grid-mode:char;punctuation-wrap:simple;
text-autospace:none'><span lang=EN-US style='font-family:宋体'>&nbsp;</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>三、</span><span style='font-family:宋体'>每支球队报名时必须符合下列要求：</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（一）</span><span style='font-family:宋体'>提交完整的俱乐部实核材料说明书；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（二）</span><span style='font-family:宋体'>报名领队<span
lang=EN-US>1</span>名、主教练员<span lang=EN-US>1</span>名、助理教练员<span lang=EN-US>1</span>至<span
lang=EN-US>2</span>名、医生<span lang=EN-US>1</span>至<span lang=EN-US>2</span>名；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（三）</span><span style='font-family:宋体'>报名运动员最少为<span
lang=EN-US>28</span>名，所有运动员均须符合本规程第五条的规定；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（四）</span><span style='font-family:宋体'>身体检测有健康隐患或疾病，不适合足球比赛的运动员不可以报名，身体健康并获得参赛资格后方可参赛。</span></p>

<p class=MsoNormal style='layout-grid-mode:char;punctuation-wrap:simple;
text-autospace:none'><span lang=EN-US style='font-family:宋体'>&nbsp;</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US>四、</span><span
style='font-family:宋体'>参赛队提供球队照片（电子版）、比赛服、球队标志和球队介绍材料。</span></p>

<p class=MsoNormal style='layout-grid-mode:char;punctuation-wrap:simple;
text-autospace:none'><span lang=EN-US>&nbsp;</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>五、</span><span style='font-family:宋体'>比赛服上衣胸前、背后、右臂袖口上可以发布球队的赞助商广告，左臂位置为校友杯比赛袖标。</span></p>

<p class=MsoNormal style='layout-grid-mode:char;punctuation-wrap:simple;
text-autospace:none'><span lang=EN-US style='font-family:宋体'>&nbsp;</span></p>

<p class=MsoNormal style='layout-grid-mode:char;punctuation-wrap:simple;
text-autospace:none'><span style='font-family:宋体'>　　</span><b><span
