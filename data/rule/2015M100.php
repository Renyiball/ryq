style='font-family:宋体'>第十三条</span></b><span lang=EN-US style='font-family:宋体'>&nbsp;
</span><span style='font-family:宋体'>竞赛规则与有关规定</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>一、</span><span style='font-family:宋体'>参照执行国际足联最新版本的《足球竞赛规则》。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>二、</span><span style='font-family:宋体'>执行北京校友体育大联盟根据国际足联或亚洲足联的要求制定的其他规定。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>三、</span><span style='font-family:宋体'>执行北京校友体育大联盟制定的《纪律准则及处罚办法》和本规程的有关规定及其他相关规定。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>四、</span><span style='font-family:宋体'>执行赛事委员会制定的校友杯足球赛文件和根据比赛中出现的问题做出的其他规定、通知和补充。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>五、</span><span style='font-family:宋体'>一些具体规定：</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（一）</span><span style='font-family:宋体'>校友杯足球赛实行检查《注册参赛资格》制度，在赛前对所有列入球队上场名单和替补名单的运动员进行资格检查，检查不合格的运动员不能参加比赛；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（二）</span><span style='font-family:宋体'>在每场比赛中，每队可报名上场运动员<span
lang=EN-US>11</span>名，替补运动员<span lang=EN-US>8</span>名，换人次数为四次，每次最多可替换两人，被替换下场的运动员可以重新替换上场比赛。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（三）</span><span style='font-family:宋体'>比赛双方球队，必须具备两套深浅两色比赛服准备比赛。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（四）</span><span style='font-family:宋体'>比赛双方球队必须穿着颜色明显不同的服装（包括上衣、短裤、球袜；守门员上衣、短裤、球袜）并经裁判员认可，</span><span
style='font-family:宋体'>比赛监督或裁判认为参赛队双方所穿比赛服颜色有可能影响摄像效果，那么比赛服颜色将予以更改，要么选择备用比赛服，要么混穿两套比赛服。</span><span
style='font-family:宋体'>两队确定比赛服装颜色后，裁判员会根据两队比赛服装颜色选择裁判服装颜色，如果裁判及助理裁判上衣服装颜色与守门员上衣服装颜色冲突，则守门员必须更换比赛服上衣颜色。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（五）</span><span style='font-family:宋体'>球衣号码的颜色应与比赛服的颜色显著不同（浅色号码在深色衣服上或相反），本条尤其适用于条纹的球衣。为保证更好的视觉效果，应在比赛服保留一块纯色的位置供印制球衣号码。</span><span
style='font-family:宋体'> </span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（六）</span><span style='font-family:宋体'>比赛服装的号码必须与报名表相符，不得更改、不得无号、不得重复；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（七）</span><span style='font-family:宋体'>足球鞋不能是金属钉或<span
lang=EN-US>Fg</span>的；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（八）</span><span style='font-family:宋体'>佩戴护腿板，如果因为不带护腿板受伤，后果完全自负。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（九）</span><span style='font-family:宋体'>不得佩戴手表、首饰、项链、硬头饰，以及其他可能伤害到人的多余或坚硬物品；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（十）</span><span style='font-family:宋体'>比赛替补席每队<span
lang=EN-US>15</span>个坐席，替补运动员<span lang=EN-US>8</span>席，球队工作人员<span lang=EN-US>7</span>席，其他人员不得入座。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:39.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>1、</span><span style='font-family:宋体'>只有参赛队正式报名人员（领队、教练员、队医）需佩带联盟通用证件，才能在替补席就座，如有关人员更换，需书面报联赛竞赛部，并制作联盟通用证件后方可在替补席就座；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:39.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>2、</span><span style='font-family:宋体'>管理好其替补席的秩序是球队领队的职责之一，本方替补席人员违纪，球队领队负连带责任；</span></p>

<p class=MsoNormal style='text-indent:21.0pt;layout-grid-mode:char;punctuation-wrap:
simple;text-autospace:none'><span style='font-family:宋体'>被裁判员罚离替补席的教练员，赛后不能出席新闻发布会，但需由领队或其他教练员代为出席。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（十一）</span><span style='font-family:宋体'>根据《纪律准则及处罚办法》，在同一赛季中，同一名运动员被出示黄牌累计三次的，处停赛<span
lang=EN-US>1</span>场；同一名运动员第一次被出示红牌的，处停赛<span lang=EN-US>1</span>场，第二次被出示红牌的，处停赛<span
lang=EN-US>2</span>场，以此类推<span lang=EN-US>……</span>详见《纪律准则及处罚办法》。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（十二）</span><span style='font-family:宋体'>比赛时不允许穿着金属钉和硬塑料钉足球鞋进行比赛，可以穿着胶钉足球鞋比赛。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（十三）</span><span style='font-family:宋体'>为保障运动员安全，比赛时不允许铲球或可能给他人造成人身伤害的行为，凡此类行为均由当值裁判员依据情节恶劣程度给予严厉处罚。</span></p>

<p class=MsoNormal style='margin-top:6.0pt;margin-right:0cm;margin-bottom:6.0pt;
margin-left:0cm;text-indent:15.75pt;layout-grid-mode:char;punctuation-wrap:
simple;text-autospace:none'><span lang=EN-US style='font-family:宋体'>&nbsp;</span><b><span
style='font-family:宋体'>第十四条</span></b><span lang=EN-US style='font-family:宋体'>&nbsp;
</span><span style='font-family:宋体'>比赛弃权和罢赛</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>一、</span><span style='font-family:宋体'>有未报名、或未通过资格审查、或未获得《注册参赛资格》、或处在停赛期、或正在诉讼过程中尚未被允许参赛的运动员，不得代表该队参加比赛，如参加比赛当场比赛按<span
lang=EN-US>0</span>：<span lang=EN-US>3</span>处理，同时，对所涉及球队和相关工作人员进行处罚。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>二、</span><span style='font-family:宋体'>有下列情况之一的球队属比赛罢赛：</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（一）</span><span style='font-family:宋体'>并非因不可抗拒的原因，且未获得校友杯足球赛赛事委员会批准，未参加赛程规定的比赛；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（二）</span><span style='font-family:宋体'>拒绝按照赛事委员会的安排参加补赛或改期的比赛；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（三）</span><span style='font-family:宋体'>拒绝按照裁判员的要求，在<span
lang=EN-US>5</span>分钟内恢复中断的比赛；</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（四）</span><span style='font-family:宋体'>中途退赛。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>三、</span><span style='font-family:宋体'>对弃权和罢赛的处理：</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:17.0pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>（一）</span><span style='font-family:宋体'>一方球队比赛弃权或罢赛，另一方球队以<span
