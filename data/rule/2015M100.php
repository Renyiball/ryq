<p class=MsoNormal style='margin-top:6.0pt;margin-right:0cm;margin-bottom:6.0pt;
margin-left:0cm;text-indent:21.0pt;layout-grid-mode:char;punctuation-wrap:simple;
text-autospace:none'><b><span style='font-family:宋体'>第四条</span></b><span
lang=EN-US style='font-family:宋体'>&nbsp; 2015</span><span style='font-family:
宋体'>校友杯足球赛</span><span style='font-family:宋体'>举办时间为</span><span lang=EN-US>2015</span><span
style='font-family:宋体'>年</span><span lang=EN-US>9</span><span style='font-family:
宋体'>月</span><span lang=EN-US>19</span><span style='font-family:宋体'>日至</span><span
lang=EN-US>2015</span><span style='font-family:宋体'>年</span><span lang=EN-US>11</span><span
