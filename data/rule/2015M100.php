<p class=MsoNormal style='text-indent:21.0pt;layout-grid-mode:char;punctuation-wrap:
simple;text-autospace:none'><b><span style='font-family:宋体'>第九条</span></b><span
lang=EN-US style='font-family:宋体'>&nbsp; </span><span style='font-family:宋体'>补充报名及报名变更</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>一、</span><span style='font-family:宋体'>主教练员、助理教练员、医生变更时，必须事先向赛事委员会申报（附证明文件），经审核批准后方能变更报名并随队比赛。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:黑体'>二、</span><span style='font-family:宋体'>补报的运动员不得使用其他运动员已经使用过的号码，只能紧接使用过的最大号码之后连续排列。补报时每名运动员应填报《补报运动员报名表》，并符合本规程第六条的规定。</span></p>

<p class=MsoNormal style='text-indent:21.0pt;layout-grid-mode:char;punctuation-wrap:
simple;text-autospace:none'><span lang=EN-US style='font-family:宋体'>&nbsp;</span></p>

<p class=MsoNormal align=center style='text-align:center;text-indent:28.05pt;
layout-grid-mode:char;punctuation-wrap:simple;text-autospace:none'><b><span
style='font-size:14.0pt;font-family:黑体'>第三章<span lang=EN-US>&nbsp; </span>竞赛及决定名次办法</span></b></p>

<p class=MsoNormal style='margin-top:6.0pt;margin-right:0cm;margin-bottom:6.0pt;
margin-left:0cm;layout-grid-mode:char;punctuation-wrap:simple;text-autospace:
none'><span lang=EN-US style='font-family:宋体'>&nbsp;&nbsp; </span><b><span
lang=EN-US style='font-family:宋体'>&nbsp;</span></b><b><span style='font-family:
宋体'>第十条</span></b><span lang=EN-US style='font-family:宋体'>&nbsp; </span><span
style='font-family:宋体'>竞赛办法和赛程安排</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>一、</span><span lang=EN-US style='font-family:宋体'>2015</span><span
style='font-family:宋体'>校友杯足球赛采用十一人制杯赛性质，及小组赛和淘汰赛两个阶段构成。小组赛阶段每个小组为<span
lang=EN-US>3</span>&#8212;<span lang=EN-US>5</span>支参赛队，小组单循环，取积分前两名出线；淘汰赛阶段采用小组间交叉淘汰进行，在规定时间内取胜者晋级出线，如打成平局，则进入加时赛和点球决胜，胜者晋级。</span></p>

<p class=MsoNormal style='margin-left:0cm;text-indent:22.7pt;layout-grid-mode:
char;punctuation-wrap:simple;text-autospace:none'><span lang=EN-US
style='font-family:宋体'>二、</span><span lang=EN-US style='font-family:宋体'>2015</span><span
