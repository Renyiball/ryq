<p class=MsoNormal style='layout-grid-mode:char;punctuation-wrap:simple;
text-autospace:none'><span lang=EN-US style='font-family:宋体'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span><span style='font-family:宋体'>详见《纪律准则及处罚办法》</span></p>

<p class=MsoNormal align=center style='margin-top:12.0pt;margin-right:0cm;
