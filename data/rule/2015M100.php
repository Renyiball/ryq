lang=EN-US style='font-family:宋体'>&nbsp; 2015</span><span style='font-family:
宋体'>校友杯足球赛由北京校友体育大联盟主办，由</span><span style='font-family:宋体'>赛事组织</span><span
style='font-family:宋体'>委员会（以下使用赛事组委会）进行组织和管理。</span></p>

<p class=MsoNormal style='margin-top:6.0pt;margin-right:0cm;margin-bottom:6.0pt;
